// ── Simple JWT ────────────────────────────────────────────────
function createToken(payload) {
  const header = Buffer.from(JSON.stringify({ alg: "HS256", typ: "JWT" })).toString("base64url");
  const body   = Buffer.from(
    JSON.stringify({ ...payload, iat: Date.now(), exp: Date.now() + 7 * 24 * 60 * 60 * 1000 })
  ).toString("base64url");
  const sig = crypto.createHmac("sha256", JWT_SECRET).update(`${header}.${body}`).digest("base64url");
  return `${header}.${body}.${sig}`;
}

function verifyToken(token) {
  try {
    const [header, body, sig] = token.split(".");
    const expectedSig = crypto
      .createHmac("sha256", JWT_SECRET)
      .update(`${header}.${body}`)
      .digest("base64url");
    if (sig !== expectedSig) throw new Error("Invalid signature");
    const payload = JSON.parse(Buffer.from(body, "base64url").toString());
    if (payload.exp < Date.now()) throw new Error("Token expired");
    return payload;
  } catch (e) {
    return null;
  }
}

// ── HTTP helpers ──────────────────────────────────────────────
function httpGet(url) {
  return new Promise((resolve, reject) => {
    const client = url.startsWith("https") ? https : http;
    client.get(url, (res) => {
      let data = "";
      res.on("data", (c) => (data += c));
      res.on("end", () => {
        try { resolve(JSON.parse(data)); }
        catch (e) { resolve({ raw: data }); }
      });
    }).on("error", reject);
  });
}

function httpPost(url, body) {
  return new Promise((resolve, reject) => {
    const payload   = JSON.stringify(body);
    const urlObj    = new URL(url);
    const options   = {
      hostname: urlObj.hostname,
      port:     urlObj.port || (url.startsWith("https") ? 443 : 80),
      path:     urlObj.pathname + urlObj.search,
      method:   "POST",
      headers:  {
        "Content-Type":   "application/json",
        "Content-Length": Buffer.byteLength(payload),
      },
    };
    const client  = url.startsWith("https") ? https : http;
    const req     = client.request(options, (res) => {
      let data = "";
      res.on("data", (c) => (data += c));
      res.on("end", () => {
        try { resolve(JSON.parse(data)); }
        catch (e) { resolve({ raw: data }); }
      });
    });
    req.on("error", reject);
    req.write(payload);
    req.end();
  });
}

// ── paySign generation (TCSAS spec, RSA-SHA256) ───────────────
// string2sign = {appId}\n{timeStamp}\n{nonceStr}\n{prepayId}\n
// signed with merchant RSA private key → base64
function generatePaySign(appId, timeStamp, nonceStr, prepayId) {
  if (!MERCHANT_PRIVATE_KEY) {
    // Development fallback — NOT for production
    console.warn(
      "  [paySign] ⚠️  MERCHANT_PRIVATE_KEY not set. Using MOCK paySign. " +
      "Set MERCHANT_PRIVATE_KEY in .env for production."
    );
    return `MOCK_PAY_SIGN_${crypto.randomBytes(8).toString("hex")}`;
  }
  const string2sign = `${appId}\n${timeStamp}\n${nonceStr}\n${prepayId}\n`;
  const sign = crypto.createSign("RSA-SHA256");
  sign.update(string2sign);
  return sign.sign(MERCHANT_PRIVATE_KEY, "base64");
}

