// POST /getUserInfo  (login — unchanged from original)
// ──────────────────────────────────────────────────────────────
app.post("/getUserInfo", async (req, res) => {
  const { appid, code } = req.body;
  if (!appid) return res.json({ code: 400, data: { msg: "Missing appid" } });
  if (!code)  return res.json({ code: 400, data: { msg: "Missing code" } });

  const url =
    `${TCSAS_OPENSERVER}/sns/jscode2session` +
    `?appid=${appid}&secret=${APPSECRET}&js_code=${code}&grant_type=authorization_code`;

  console.log("  Calling jscode2session:", url);

  let sessionData;
  try {
    sessionData = await httpGet(url);
    console.log("  jscode2session response:", JSON.stringify(sessionData));
  } catch (err) {
    return res.json({ code: 500, data: { msg: "Failed to contact TCSAS: " + err.message } });
  }

  if (sessionData.errcode && sessionData.errcode !== 0) {
    return res.json({ code: 401, data: { msg: `TCSAS error ${sessionData.errcode}: ${sessionData.errmsg}` } });
  }

  const { openid } = sessionData;
  if (!openid) return res.json({ code: 401, data: { msg: "No openid returned" } });

  let user = users[openid];
  if (!user) {
    user = {
      openid,
      id:        uuidv4(),
      userName:  `User_${openid.substring(0, 8)}`,
      avatarUrl: "https://picsum.photos/100",
      phone:     "",
      email:     "",
      createdAt: Date.now(),
    };
    users[openid] = user;
    console.log(`  New user created: openid=${openid}`);
  }

  const token = createToken({ openid, userId: user.id });

  return res.json({
    code: 200,
    data: {
      userName:  user.userName,
      token,
      id:        user.id,
      avatarUrl: user.avatarUrl,
      phone:     user.phone,
      email:     user.email,
      account:   openid,
    },
  });
});

// ──────────────────────────────────────────────────────────────
// POST /payOrderV3  — Stage 1: Pre-order generation
//
// Called by: Mini program frontend (via utils/fetch.js commonPay)
// This endpoint:
//   1. Validates JWT token
//   2. Calls SAS /v3/pay/transactions/jsapi to get prepayId
//   3. Generates paySign using merchant RSA private key
//   4. Returns payment params to mini program for wx.requestPayment()
// ──────────────────────────────────────────────────────────────
app.post("/payOrderV3", async (req, res) => {
  const { goods_detail, discount = 0, token, appid } = req.body;

  // ── 1. Auth ────────────────────────────────────────────────
  if (!token) return res.json({ code: 401, data: { msg: "Missing token — please login first" } });
  const payload = verifyToken(token);
  if (!payload) return res.json({ code: 401, data: { msg: "Invalid or expired token" } });
  const user = users[payload.openid];
  if (!user) return res.json({ code: 401, data: { msg: "User not found" } });

  if (!goods_detail || goods_detail.length === 0) {
    return res.json({ code: 400, data: { msg: "No goods in order" } });
  }

  // ── 2. Build order ─────────────────────────────────────────
  const out_trade_no = `order_${Date.now()}_${crypto.randomBytes(4).toString("hex")}`;
  const timeStamp    = Math.floor(Date.now() / 1000).toString();
  const nonceStr     = crypto.randomBytes(10).toString("hex");

  // Calculate total amount in cents (unit_price is in dollars, ×100 for cents)
  const totalCents = goods_detail.reduce(
    (sum, item) => sum + Math.round((item.unit_price || 0) * 100) * (item.quantity || 1),
    0
  );
  const discountCents = Math.round(discount * 100);
  const finalAmount   = Math.max(totalCents - discountCents, 1); // minimum 1 cent

  console.log(`  [payOrderV3] out_trade_no=${out_trade_no} amount=${finalAmount} openid=${payload.openid}`);

  // ── 3. Call SAS to get prepayId ────────────────────────────
  // SAS endpoint: POST /v3/pay/transactions/jsapi
  // SAS will forward to superapp-backend /v3/pay/transactions/jsapi
  const sasPayload = {
    appid:         APPID,
    description:   goods_detail.map((g) => g.goods_name).join(", ").substring(0, 100),
    out_trade_no,
    notify_url:    `${MINIAPP_BACKEND_URL}/notify_payBack`,
    amount: {
      total:    finalAmount,
      currency: "SGD",        // adjust currency for your region
    },
    payer: {
      openid: payload.openid,
    },
    goods_detail,
  };

  let prepayId;
  try {
    console.log(`  [payOrderV3] Calling SAS /v3/pay/transactions/jsapi ...`);
    const sasResp = await httpPost(
      `${TCSAS_OPENSERVER}/v3/pay/transactions/jsapi`,
      sasPayload
    );
    console.log(`  [payOrderV3] SAS response:`, JSON.stringify(sasResp));

    // SAS returns { returnCode: "0", data: { prepayId: "..." } }
    if (sasResp.returnCode !== "0" && sasResp.code !== 200) {
      console.error("  [payOrderV3] SAS returned error:", JSON.stringify(sasResp));
      // ── Development fallback: if SAS not reachable, use mock prepayId ──
      prepayId = `prepay_mock_${Date.now()}_${crypto.randomBytes(8).toString("hex")}`;
      console.warn("  [payOrderV3] Using MOCK prepayId (SAS not reachable in dev)");
    } else {
      prepayId = sasResp.data?.prepayId || sasResp.prepayId;
      if (!prepayId) {
        console.error("  [payOrderV3] No prepayId in SAS response. Using mock.");
        prepayId = `prepay_mock_${Date.now()}_${crypto.randomBytes(8).toString("hex")}`;
      }
    }
  } catch (err) {
    console.error("  [payOrderV3] SAS call failed:", err.message);
    // Development fallback
    prepayId = `prepay_mock_${Date.now()}_${crypto.randomBytes(8).toString("hex")}`;
    console.warn("  [payOrderV3] Using MOCK prepayId (SAS unreachable)");
  }

  // ── 4. Generate paySign with merchant RSA private key ──────
  // Per TCSAS docs: string2sign = appId\ntimeStamp\nnonceStr\nprepayId\n
  const paySign = generatePaySign(APPID, timeStamp, nonceStr, prepayId);

  // ── 5. Save order state for later notification/polling ─────
  orders[out_trade_no] = {
    out_trade_no,
    openid:     payload.openid,
    prepayId,
    status:     "PENDING",   // PENDING | SUCCESS | FAILED
    amount:     finalAmount,
    goods:      goods_detail,
    createdAt:  Date.now(),
  };

  console.log(`  [payOrderV3] SUCCESS prepayId=${prepayId}`);

  // ── 6. Return to mini program ──────────────────────────────
  // Mini program passes these directly to wx.requestPayment()
  return res.json({
    code:      200,
    timeStamp,
    nonceStr,
    package:   `prepay_id=${prepayId}`,
    signType:  "RSA",
    paySign,
    // Extra: pass out_trade_no so frontend can poll later
    out_trade_no,
  });
});

